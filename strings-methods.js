"use strict";
let nombre = "Jorge";
/*
endswith()
lastIndexOf()
slice()
replace()
includes()
*/
console.log(nombre.endsWith("e")); // True
console.log(nombre.lastIndexOf("o")); // 1
console.log(nombre.slice(0, 3)); // "Jor"
console.log(nombre.replace("e", "ito")); // "Jorgito"
console.log(nombre.includes("J")); // true
